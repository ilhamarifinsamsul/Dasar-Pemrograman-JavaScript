// Comparison Operator : Operator perbandingan adalah operator yang digunakan untuk membandingkan dua nilai dan mengembalikan nilai boolean (true atau false) sebagai hasil perbandingan
const a = 10;
const b = 12;

console.log(a < b); // output: true
console.log(a > b); // output: false

// Logical Operator
console.log(true && true); // true
console.log(false && true); // false
console.log(true && false); // false
console.log(false && false); // false
console.log(5 === 5 && 3 < 5); // true

// OR
console.log(true || true); // true
console.log(false || true); // true
console.log(true || false); // true
console.log(false || false); // false
console.log(5 === 5 || 3 > 5); // true

// NOT
console.log(!true); // false
console.log(!false); // true

// String Operator
